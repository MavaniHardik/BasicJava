package lectureTwoDataType;
// boolean char byte short int long float double
public class PrimitvieData {
    public static void main(String[] args) {
        boolean areYouPass=true; // true false
        char whichClass='9';   // only single
        byte examTime=120;   //127  size 1byte
        short dateofB=12346; // 5 digit  size 2 byte
        int date=28121996;  // 10 digit  size 4 byte
        long contact=978822554465L;  //  20 digit size 8 byte
        float classResult=97.225555555f;
        double totalResult=96.576899627634863648664d;

        System.out.println(areYouPass);
        System.out.println(whichClass);
        System.out.println(examTime);
        System.out.println(dateofB);
        System.out.println(date);
        System.out.println(contact);
        System.out.println(classResult);
        System.out.println(totalResult);

    }
}
