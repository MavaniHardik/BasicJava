package lectureThreeOperators;

public class Arithmatic2 {
    public static void main(String[] args) {
        //division

        float a=12;
        float b=5;
        float c=a/b;
        System.out.println(a/b);
        System.out.println(c);

        //Modulus
        int x=12;
        int y=5;
        int z=x%y;
        System.out.println(x%y);
        System.out.println(z);

    }
}
