package lecture6Conditional;

public class ForLoop {
    public static void main(String[] args) {
        // flow
        // start >> condition/ending check >>> statement >> increment
        for(int a=1;a<=10;a++){
            // start condition check then incriment
            System.out.println("Hardik");
        }
        for (int e=1;e<=10;e++){//1,2,3,4,....8,9,10
            System.out.println(e);
        }
        for (int c=10;c>=0;c--){  // 10,9,8,7,6....1,0
            System.out.println(c);
        }
    }
}
